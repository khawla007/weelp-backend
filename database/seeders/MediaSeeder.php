    /**
     * Generate a human-readable name from the file path.
     * e.g. "countries/france_1_1773038620.jpg" → "France - Image 1"
     *      "cities/paris-3.jpg"                 → "Paris - Image 3"
     */
    private static function generateName(string $path): string
    {
        $filename = pathinfo($path, PATHINFO_FILENAME);
        $parts = explode('/', $path);
        $folder = ucfirst($parts[0]);

        if (str_starts_with($path, 'countries/random-tourist-places/')) {
            $country = ucwords(str_replace('-', ' ', $parts[2] ?? 'country'));
            if (preg_match('/^(\d+)-/', $filename, $m)) {
                return "{$country} Tourist Place {$m[1]}";
            }
            return "{$country} Tourist Place";
        }

        // designs: "image-import-10" → "Design Image 10"
        if (str_starts_with($path, 'designs/') && preg_match('/^image-import-(\d+)$/i', $filename, $m)) {
            return "Design Image {$m[1]}";
        }

        // designs fallback: anything else under designs/ → humanised filename
        if (str_starts_with($path, 'designs/')) {
            return ucwords(str_replace(['-', '_'], ' ', $filename));
        }

        // countries/states: "france_1_1773038620" → extract "france" and "1"
        if (preg_match('/^(.+?)_(\d+)_\d+$/', $filename, $m)) {
            $name = ucwords(str_replace('-', ' ', $m[1]));
            return "{$name} - Image {$m[2]}";
        }

        // cities: "paris-3" → "Paris - Image 3"
        if (preg_match('/^(.+?)[-_](\d+)$/', $filename, $m)) {
            $name = ucwords(str_replace('-', ' ', $m[1]));
            return "{$name} - Image {$m[2]}";
        }

        // avatars: "4" → "Avatar 4"
        if ($folder === 'Avatars') {
            return "Avatar {$filename}";
        }

        // media: random hash → "Media Upload"
        return "Media Upload";
    }

